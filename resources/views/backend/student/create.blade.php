                url:"{{ url('panel/student/getclass') }}",
                type:"POST",
                data:{
                    _token:"{{ csrf_token() }}",
                    school_id:school_id,
                },
                dataType:"json",
                success:function(response){
                    $('.getClass').html(response.success);
                },
            });
        });
    </script>

